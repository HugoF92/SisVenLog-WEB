package Converter;

import entidad.TiposVentas;
import entidad.TiposVentasPK;
import java.io.Serializable;
import javax.faces.bean.ApplicationScoped;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.inject.Named;

/**
 *
 * @author SAMACACE
 */
@Named(value = "TiposVentasConverter")
@ApplicationScoped

public class TiposVentasConverter  implements Converter, Serializable{
    
   @Override
    public Object getAsObject(FacesContext context, UIComponent component, String value)
    {
        if (value.trim().equals(""))
        {
            return null;
        }
        else
        {
            //return new TiposVentas(value);
            return new TiposVentas(new TiposVentasPK(new Short("2"), value.charAt(0)));
        }
    }

    @Override
    public String getAsString(FacesContext context, UIComponent component, Object value)
    {
        if (value == null || value.equals(""))
        {
            return "";
        }
        else
        {
            return String.valueOf(((TiposVentas) value).getTiposVentasPK().getCtipoVta());
        }
    }

    
}
